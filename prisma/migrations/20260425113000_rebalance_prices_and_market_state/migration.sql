UPDATE "MarketProductState" market_state
SET
  "marketAveragePrice" = price_updates."basePrice",
  "currentSupplierPrice" = GREATEST(
    60,
    ROUND(
      price_updates."basePrice" *
        CASE product."category"::text
          WHEN 'FRUIT_AND_VEGETABLES' THEN 0.72
          WHEN 'BAKERY_AND_GRAINS' THEN 0.74
          WHEN 'PANTRY_AND_COOKING' THEN 0.76
          WHEN 'DRINKS' THEN 0.75
          WHEN 'MEAT_DAIRY_AND_PROTEIN' THEN 0.78
          WHEN 'SNACKS_AND_SWEETS' THEN 0.77
          WHEN 'KITCHEN_AND_COOKWARE' THEN 0.71
          WHEN 'CLEANING_AND_PERSONAL_CARE' THEN 0.73
          WHEN 'CLOTHING' THEN 0.68
          WHEN 'HOME_AND_STORAGE' THEN 0.69
          WHEN 'ELECTRONICS' THEN 0.88
          WHEN 'SCHOOL_AND_MISC' THEN 0.72
          ELSE 0.74
        END
    )::int
  )
FROM price_updates
INNER JOIN "Product" product ON product."name" = price_updates."name"
WHERE market_state."productId" = product."id";
